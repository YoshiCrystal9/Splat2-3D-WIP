class GX2SurfaceUse(enum.IntFlag):
    TEXTURE = 1 << 0
    COLOR_BUFFER = 1 << 1
    DEPTH_BUFFER = 1 << 2
    SCAN_BUFFER = 1 << 3
    FINAL_TV = 1 << 31
    COLOR_BUFFER_TEXTURE = TEXTURE or COLOR_BUFFER
    DEPTH_BUFFER_TEXTURE = TEXTURE or DEPTH_BUFFER
    COLOR_BUFFER_FINAL_TV = FINAL_TV or COLOR_BUFFER
    COLOR_BUFFER_TEXTURE_FINAL_TV = FINAL_TV or COLOR_BUFFER_TEXTURE


